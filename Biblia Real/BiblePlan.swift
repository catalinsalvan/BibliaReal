import Foundation

struct PlanPassage: Hashable {
    let bookId: Int
    let chapters: [Int]

    var firstChapter: Int { chapters.first ?? 1 }

    func label(using bookNames: [Int: String]) -> String {
        let name = bookNames[bookId] ?? "#\(bookId)"
        guard !chapters.isEmpty else { return name }
        if chapters.count == 1 { return "\(name) \(chapters[0])" }
        let consecutive = zip(chapters, chapters.dropFirst()).allSatisfy { $1 == $0 + 1 }
        if consecutive { return "\(name) \(chapters.first!) – \(chapters.last!)" }
        return "\(name) \(chapters.map(String.init).joined(separator: ", "))"
    }
}

